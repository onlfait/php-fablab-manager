  <h1 class="title">C'est quoi un Fablab</h1>

  <p>
    Fablab (contraction des mots Fabrication et Laboratoire) est un lieu abordable,
    ouvert au public mettant à la disposition de ses utilisateurs les ressources techniques,
    technologiques et humaines (machines, outils, logiciels, procédés, savoir-faire, mentors)
    nécessaires à la conception, l’optimisation, la réparation de toute sorte d’objets.
  </p>

  <p>
    Un fablab c’est aussi et surtout un lieu de partage où les membres sont tour à tour bénéficiaires et contributeurs,
    où les expériences sont mises en communs afin d’optimiser le potentiel global d’innovation.
  </p>

  <p>
    Les fablabs constituent également un réseau mondial et dans cette optique de partage des connaissances (open-source/open-hardware),
    les fablabs de chaque pays, ville, quartier sont en contact et travaillent ensemble en partageant le fruit de leurs recherches et développements.
  </p>

