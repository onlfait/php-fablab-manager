    <p class="SousTitreC">Astroplant</p>
    <p class="TexteC">L'Agence Spatiale Européenne a lancé un projet pédagogique de
      science citoyenne pour unir ses forces avec le grand public et les étudiants. <br>
      À l'aide d'une serre de bureau bricolée contenant plusieurs capteurs, les
      « citoyens scientifiques » feront pousser des plantes pour tester des cultures
      potentielles pour l'espace et faire partie de la communauté enthousiaste de
      jardiniers DIY urbains. <br>
      En collaboration avec la Loterie Romande, le Departement de l'Instruction Publique
      et la Confederation Suisse (Swiss Space Office), Onl'Fait a lancé Astroplant
      en 4 écoles genevoises. <br>
      Pus d'info sur le projet Astroplant sous: <a href="http://www.astroplant.io" target="_blank"> www.astroplant.io </a> </p>

